# Print 'Hello' multiple time

for i in range (10):
    print('Hello')

# Print 'Hello' multiple time with number

for i in range (5):
    print(i, 'Hello')

# Print even number

for i in range (10):
    if i % 2 == 0:
        print(i)

# Print odd number

for i in range (10):
    if i % 2 != 0:
        print(i)